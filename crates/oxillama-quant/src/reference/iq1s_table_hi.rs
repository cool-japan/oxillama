/// Look up a value in the full IQ1S grid (indices 0..2048).
///
/// This combines `IQ1S_GRID_LO` and `IQ1S_GRID_HI` into a single accessor.
#[inline]
pub fn iq1s_grid(index: usize) -> u64 {
    if index < 1024 {
        super::iq1s_table_lo::IQ1S_GRID_LO[index]
    } else {
        IQ1S_GRID_HI[index - 1024]
    }
}
